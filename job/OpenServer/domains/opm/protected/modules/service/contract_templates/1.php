<p>
	Micropigmentation is the process of placing pigment into the dermis. These pigments can last a few years or indefinitely, depending on skin tone and the intensity of the color that is used. Lighter skin tones will not hold the color as long as darker skin tones and may require an occasional touch-up to prolong the results of the procedure. Initially the color will appear much more vibrant or darker compared to the end result. Usually within 5- 7 days the color will fade 10-50%, soften and look more natural.
</p>


<h2>Possible risks, hazards or complications</h2>

<p>
	<i>Pain</i>: There can be pain even after the topical anesthetic has been used. Anesthetics work better on some people than others. Lip procedures are more likely to involve some pain.
</p>
<p>
	<i>Infection</i>: Infection is very unusual. The areas treated must be kept clean and only freshly cleaned hands should touch the areas. See "After Care" sheet for instructions on care.
</p>
<p>
	<i>Uneven Pigmentation</i>: This can result from poor healing, infection, bleeding or many other causes. Your follow up appointment will likely correct any uneven appearance.
</p>
<p>
	<i>Asymmetry</i>: Every effort will be made to avoid asymmetry but our faces are not symmetrical so adjustments may be needed during the follow up session to correct any unevenness.
</p>
<p>
	<i>Excessive Swelling or Bruising</i>: Some people bruise and swell more than others. Ice packs may help and the bruising and swelling typically disappears with 1-5 days. Some people don't bruise or swell at all.
</p>
<p>
	<i>Eye Exposure</i>: There is a small risk of eye injury when an eyeliner procedure is performed. To avoid corneal abrasion, Celluvisc, a thick eye drop is used to protect the eye prior to the procedure. Eye drops are used to cleanse and flush the eye after the procedure is complete.
</p>
<p>
	<i>Anesthesia</i>: Topical anesthetics are used to numb the area to be treated. Lidocaine, Prilocaine,Benzocaine, Tetracaine and Epinephrine in a cream or gel form are typically used. If you areallergic to any of these please inform us now.
</p>
<p>
	<i>Fever Blisters</i>: If you are prone to cold sores or fever blisters, (herpes simplex), there is a highprobability that you will get them. It is advised that you call your doctor for a prescription anti-viral to help prevent this from occurring.
</p>
<p>
	<i>Allergic Reaction</i>: There is a small possibility of an allergic reaction.
</p>

