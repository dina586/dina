<h2>BACKGROUND</h2>

<p>
	BOTOX relaxes the muscles of the face that cause wrinkles associated with facial expressions. Treatment can smooth these expression lines. Areas most frequently treated are:  the glabellar area (located between the eyes); crow’s feet (lateral areas of the eyes); and forehead wrinkles. Botox and Dysport are diluted to precise concentrations and injected into the muscles with a very thin needle.  There is minimal discomfort. Clients may feel a slight stinging sensation as the solution is being injected. The procedure takes about 15-30 minutes and the results can last 3-5 months. With repeated treatments, the results tend to last longer.
</p>

<h2>RISKS AND COMPLICATIONS</h2>

<p>
	There are certain inherent and potential risks and side effects in any invasive procedure and, in this specific instance, such risks include but are not limited to: 1) Post treatment discomfort, swelling, redness and bruising, 2) Post treatment bacterial, viral and/or fungal infection requiring further treatments, 3) Allergic reaction, 4) Minor temporary droop of eyelid(s) in approximately 2% of injection (can be treated with eye drops in many cases), 5) Occasional numbness of the forehead lasting up to 2-3 weeks, 6) Transient headache, and 7) Flu-like symptoms.
</p>

